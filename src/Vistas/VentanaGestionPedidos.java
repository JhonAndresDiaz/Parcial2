    private void cboClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboClientesActionPerformed
        Object selectedItem = cboClientes.getSelectedItem();
        if (selectedItem != null && selectedItem.toString().equals("Añadir Categoria")) {
            String recibido = JOptionPane.showInputDialog(null, "Ingrese el nombre del nuevo cliente");
            int id_cliente = Integer.parseInt(recibido);
            if (id_cliente != 0) {
                try {
                    controlador.aniadirCliente(id_cliente);
                    String nombre_categoria = obtenerNombreCliente(id_cliente);                    System.out.println(nombre_categoria);
                    actualizarComboBox();
                    String categoriaFormateada = nombre_categoria;
                    cboClientes.setSelectedItem(categoriaFormateada);
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_cboClientesActionPerformed

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        if(txtIdPedido.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Ingrese un numero de id en el campo de texto para eliminar el cliente");
        }else{
            try {
                int pedido_id = Integer.parseInt(txtIdPedido.getText());
                controlador.eliminarPedido(pedido_id);
                limpiarCampos();
                actualizarTabla();
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        if(txtFecha.getText().isEmpty() || txtTotal.getText().isEmpty()  ){
            JOptionPane.showMessageDialog(rootPane, "Ingrese todos los campos de texto para actualizar los datos del pedido en la base de datos");
            return;
        }else {
            try {
                int pedido_id = Integer.parseInt(txtIdPedido.getText());
                String fecha = txtFecha.getText();
                float total = Float.parseFloat(txtTotal.getText());
                int id = Integer.parseInt(cboClientes.getSelectedItem().toString());
                Pedido pedido = new Pedido(fecha, total, id);
                controlador.editarPedido(pedido, pedido_id);
                limpiarCampos();
                actualizarTabla();  
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnEditarMouseClicked

