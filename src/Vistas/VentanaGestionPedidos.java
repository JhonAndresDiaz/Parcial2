    private void btBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btBuscarMouseClicked
        if(Txt_id_Buscar.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Escriba un id en el campo de texto para buscarlo correctamente.");
            return;
        }
        try{
            int pedido_id = Integer.parseInt(Txt_id_Buscar.getText());
            Pedido pedido = controlador.buscarPedido(pedido_id);
            if (pedido != null) {
                txtFecha.setText(pedido.getFecha_pedido());
                txtTotal.setText(String.valueOf(pedido.getTotal()));
                txtIdPedido.setText(String.valueOf(pedido.getPedido_id()));
                String idCliente = String.valueOf(pedido.getId());
                cboClientes.setSelectedItem(idCliente);
                cboClientes.setEnabled(false);
            } else {
                limpiarCampos();
            }
        }catch(Exception e){
        }
    }//GEN-LAST:event_btBuscarMouseClicked

